void DisplayTextBuffer()
{
    uint8_t txIndex;

    for (int line=0; line<8; line++)
    {
        txIndex = 0;
        uint8_t transmitBuffer[129];
        transmitBuffer[txIndex] = 0x40;
        txIndex++;
        for (int character=0; character<COLUMNS; character++) 
        {
            for (int offset=0; offset<5; offset++)
            {
                int fontData = font[(uint8_t)textBuffer[line*COLUMNS+character]*5+offset];
                transmitBuffer[txIndex] = fontData;
               txIndex++;
             }
            transmitBuffer[txIndex] = 0;
            txIndex++;
        }

        transmitBuffer[127] = 0;
        transmitBuffer[128] = 0;

        nrf_drv_twi_tx(&display_twi, DISPLAY_ADDRESS, transmitBuffer, 129, false);
    }
}


