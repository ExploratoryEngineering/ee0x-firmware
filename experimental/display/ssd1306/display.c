void ReverseBitsInBuffer()
{
     // Reverse bits (in case of MSB/LSB confusion)
     for (int i=0; i<DISPLAYBUFFER_SIZE; i++)
     {
         buffer[i] = BitReverseTable256[buffer[i]] & 0xFF;
     }
}


 void InitDisplayBuffer()
 {
     for (int row=0; row <64; row++)
     {
         cmd_buffer[row*17] = 0x40;
         for (int col=0; col<16; col++)
         {
            cmd_buffer[row*17+col+1] = buffer[row*16+col];
         }
     }
 }


void ClearDisplayBuffer()
 {
     for (int row=0; row <64; row++)
     {
         cmd_buffer[row*17] = 0x40;
         for (int col=0; col<16; col++)
         {
            cmd_buffer[row*17+col+1] = 0;
         }
     }
 }


