void Reset()
{
    nrf_drv_gpiote_out_set(DISPLAY_RESET);
    //VDD (3.3V) goes high at start, lets just chill for a ms
    nrf_delay_ms(1);
    // bring reset low
    nrf_drv_gpiote_out_clear(DISPLAY_RESET);
    // wait 10ms
    nrf_delay_ms(100);
    // bring out of reset
    nrf_drv_gpiote_out_set(DISPLAY_RESET);
     // turn on VCC (9V?)
}

